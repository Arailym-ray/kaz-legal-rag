USER_PROMPTS = {
    "kz": {
        "LLM-only": "Сұрақ:\n{q}",
        "RAG":      "Сұрақ:\n{q}\n\nДереккөздер:\n{e}",
    },
    "rus": {
        "LLM-only": "Вопрос:\n{q}",
        "RAG":      "Вопрос:\n{q}\n\nИсточники:\n{e}",
    },
}


def build_messages(setting: str, lang: str, complexity: str, question: str, evidence: str = ""):
    """Return (system_prompt, user_prompt).

    Parameters
    ----------
    setting : "LLM-only" or "RAG"
    lang : "kz" or "rus"
    complexity : "simple" / "moderate" / "complex"  (controls the system prompt)
    question : the legal question
    evidence : retrieved evidence context (RAG only)
    """
    sys_map = SYSTEM_PROMPTS_KZ if lang == "kz" else SYSTEM_PROMPTS_RUS
    system = sys_map.get(complexity, sys_map["simple"])
    user = USER_PROMPTS[lang][setting].format(q=question, e=evidence)
    return system, user
